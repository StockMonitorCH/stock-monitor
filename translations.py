# ── Aktive Sprache (wird von config.py gesetzt) ───────────────────────────────
_CURRENT_LANG: str = "DE"


def set_language(lang: str) -> None:
    """Setzt die aktive Sprache. Gültige Werte: 'DE', 'EN'"""
    global _CURRENT_LANG
    if lang in STRINGS:
        _CURRENT_LANG = lang
        # Steuermodul-Übersetzungen synchron umstellen
        try:
            from tax_translations import set_tax_language
            set_tax_language(lang)
        except ImportError:
            pass  # tax_module nicht vorhanden – kein Fehler


def get_language() -> str:
    return _CURRENT_LANG


def TR(key: str, **kwargs) -> str:
    """
    Gibt den übersetzten String zurück.
    Fallback: erst DE, dann der Key selbst (damit nichts leer bleibt).
    kwargs werden als .format()-Argumente übergeben, z.B. TR("msg_xyz", name="AAPL")
    """
    lang_dict = STRINGS.get(_CURRENT_LANG, STRINGS["DE"])
    text = lang_dict.get(key) or STRINGS["DE"].get(key) or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text


